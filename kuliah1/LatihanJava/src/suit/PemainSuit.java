
package suit;

public class PemainSuit {
    private String namaPemain;
    private String attributPemain;
    
    public void setNamaPemain(String namaPemain){
        this.namaPemain = namaPemain;
    }
    
    public void setAttributPemain(String attributPemain){
        this.attributPemain = attributPemain;
    }
    
    public String getAttribut(){
        return this.attributPemain;
    }
    
    public String getNama(){
        return this.namaPemain;
    }
    
}
